int adc_get_battery_voltage(int* vbat)
{
    HAL_ADC_Start(&hadc2);
    uint32_t tmpval = 0;
    if (HAL_ADC_PollForConversion(&hadc2, 100) == HAL_OK)
    {
      tmpval = HAL_ADC_GetValue(&hadc2);

      tmpval = 4950*tmpval; //3/2*3300
      *vbat = tmpval/4096;

      //battery voltage resistive divider: 330k + 100k 
      //ADC resolution: 12bits
      //VDDA = VREF+ = 3300 mV

      HAL_ADC_Stop(&hadc2);

      return 0;
    }

    HAL_ADC_Stop(&hadc2);

    return -1;
}

int adc_get_battery_level()
{
  int retval = 0;
  adc_get_battery_voltage(&retval);
  retval = (retval-3300)*100/(4150-3300);
  if(retval<0)
    retval = 0;
  else if(retval>100)
    retval = 100;
  return retval;
}
